    private void btnComprobarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComprobarActionPerformed
    String nombreCategoria = comboBoxCategoria.getSelectedItem().toString();
        Categoria categoria = cat_dao.obtenerCategoriaPorNombre(nombreCategoria);
        if (categoria != null) {
            c = categoria;
            txtNombreCategoria.setText(categoria.getNombre());
            txtDescripCategoria.setText(categoria.getDescripcion());
        } else {
            JOptionPane.showMessageDialog(null, "La categoría no existe en la base de datos.");
        }
    }//GEN-LAST:event_btnComprobarActionPerformed

