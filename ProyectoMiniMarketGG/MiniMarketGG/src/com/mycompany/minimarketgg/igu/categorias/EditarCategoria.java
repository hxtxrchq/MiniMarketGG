    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
     if (c != null) {
            try {
                // Actualizar los detalles de la categoría con los nuevos valores de los campos de texto
                c.setNombre(txtNombreCategoria.getText());
                c.setDescripcion(txtDescripCategoria.getText());

                // Llamar al método para guardar los cambios en la base de datos
                if (cat_dao.actualizarCategoria(c)) {
                    JOptionPane.showMessageDialog(null, "Cambios guardados correctamente.");
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Error al guardar los cambios.");
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Error en la entrada de datos.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "No se ha cargado ninguna categoría para guardar los cambios.");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

