    private void cargarProductos() {
        comboBoxAgregarProdPed.removeAllItems();
        int idProveedor = pedidoDao.obtenerPedidoPorId(idPedido).getIdProveedor();
        List<String> nombresProductos = productodao.obtenerNombresProductosPorProveedor(idProveedor);
        for (String nombreProducto : nombresProductos) {
            comboBoxAgregarProdPed.addItem(nombreProducto);
        }
        comboBoxAgregarProdPed.addActionListener(e -> llenarPrecioProveedor());
    }
      private int obtenerIdProductoDesdeNombre(String nombreProducto) {
        producto = productodao.obtenerProductoPorNombre(nombreProducto);
        return producto != null ? producto.getIdProducto() : -1;
    }

    private void llenarPrecioProveedor() {
        String nombreProducto = (String) comboBoxAgregarProdPed.getSelectedItem();
        if (nombreProducto != null) {
            double precioProveedor = productodao.obtenerPrecioProveedorPorNombre(nombreProducto);
            if (precioProveedor != -1) {
                txtPunit.setText(String.valueOf(precioProveedor));
                txtPunit.setEditable(false); // Hacer que el campo de texto no sea editable después de llenarlo
            } else {
                txtPunit.setText("");
                txtPunit.setEditable(true); // Permitir edición si no se encuentra el precio
            }
        }
    }

    
