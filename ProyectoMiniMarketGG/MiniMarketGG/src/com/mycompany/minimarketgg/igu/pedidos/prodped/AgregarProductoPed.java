    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
        int cantidad = Integer.parseInt(txtCant.getText().trim());
        String nombreProducto = comboBoxAgregarProdPed.getSelectedItem().toString();
        int idProducto = productodao.obtenerIdPorNombre(nombreProducto);
        double precioUnitario = productodao.obtenerPrecioProveedorPorNombre(nombreProducto); // Obtener el precio del proveedor

            if (idProducto != -1 && precioUnitario != -1) {
                productopedido = new ProductoPedido(idProducto, idPedido, cantidad, precioUnitario);
                if (productopedidodao.registrarProductoPedido(productopedido)) {
                    JOptionPane.showMessageDialog(this, "Producto añadido al pedido correctamente.");
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Error al añadir producto al pedido.");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Producto no encontrado o precio del proveedor no disponible.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese valores numéricos válidos.");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

