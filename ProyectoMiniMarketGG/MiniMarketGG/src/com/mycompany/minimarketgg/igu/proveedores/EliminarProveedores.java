    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String nombreProveedor = (String) comboBoxProveedor.getSelectedItem();
        if (nombreProveedor != null) {
            if (chkConfirmacion.isSelected()) {
                // La casilla de verificación está marcada, procede con la eliminación del proveedor
                ProveedorDao proveedorDao = new ProveedorDao();
                Proveedor proveedor = proveedorDao.obtenerProveedorPorNombre(nombreProveedor);
                if (proveedor != null) {
                    // Proveedor encontrado, proceder con la eliminación
                    if (proveedorDao.eliminarProveedor(proveedor.getId())) {
                        JOptionPane.showMessageDialog(null, "Proveedor eliminado correctamente.");
                        limpiarFormulario();
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(null, "Error al eliminar el proveedor.");
                        limpiarFormulario();
                    }
                } else {
                    // Proveedor no encontrado
                    JOptionPane.showMessageDialog(null, "El proveedor no existe en la base de datos.");
                }
            } else {
                // La casilla de verificación no está marcada, informar al usuario que debe confirmar la eliminación
                JOptionPane.showMessageDialog(null, "Por favor, marque la casilla de confirmación para eliminar el proveedor.");
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

