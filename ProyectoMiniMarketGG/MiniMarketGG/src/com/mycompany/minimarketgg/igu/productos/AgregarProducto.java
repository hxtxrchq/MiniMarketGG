    private boolean camposCompletos() {
        return !txtNombre.getText().isEmpty() && 
               !comboBoxCategoria.getSelectedItem().toString().isEmpty() && 
               !txtStock.getText().isEmpty() && 
               !comboBoxProveedor.getSelectedItem().toString().isEmpty() && 
               !txtDescripcion.getText().isEmpty();
    }
    
     private void llenarComboBoxProveedor() {
        // Llenar el JComboBox con los nombres de los proveedores
        ArrayList<String> nombresProveedores = pvd.obtenerNombresProveedores();
        for (String nombre : nombresProveedores) {
            comboBoxProveedor.addItem(nombre);
        }
    }
     
     private void llenarComboBoxCategoria() {
    // Llenar el JComboBox con los nombres de las categorías 
    ArrayList<String> nombresCategorias = cad.obtenerNombresCategorias();
    for (String nombreCategoria : nombresCategorias) {
        comboBoxCategoria.addItem(nombreCategoria);
    }
}

