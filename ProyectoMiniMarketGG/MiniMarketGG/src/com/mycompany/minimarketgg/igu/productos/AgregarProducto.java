    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
         if (!camposCompletos()) {
        JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.");
        return;  // Asegura que el método no continúe si los campos no están completos.
    }

    String nombreCategoria = comboBoxCategoria.getSelectedItem().toString();
    String nombreProveedor = comboBoxProveedor.getSelectedItem().toString();

    Categoria categoria = cad.obtenerCategoriaPorNombre(nombreCategoria);
    if (categoria == null) {
        JOptionPane.showMessageDialog(this, "Categoría no encontrada.");
        return;
    }
    
    Proveedor proveedor = pvd.obtenerProveedorPorNombre(nombreProveedor);
    if (proveedor == null) {
        JOptionPane.showMessageDialog(this, "Proveedor no encontrado.");
        return;
    }
    
    try {
        pr.setNombre(txtNombre.getText());
        pr.setDescripcion(txtDescripcion.getText());
        pr.setPrecio(Double.parseDouble(txtPrecio.getText()));  // Asegurarse de que el precio es un número válido.
        pr.setStock(Integer.parseInt(txtStock.getText()));  // Asegurarse de que el stock es un número válido.
        pr.setIdCategoria(categoria.getId());
        pr.setIdProveedor(proveedor.getId());

        if (prd.RegistrarProducto(pr)) {
            JOptionPane.showMessageDialog(this, "Producto guardado correctamente.");
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Error al guardar el producto.");
        }
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Error en los datos numéricos: " + ex.getMessage());
    }
    }//GEN-LAST:event_btnGuardarActionPerformed

