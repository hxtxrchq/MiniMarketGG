//metodo para calcular el total a pagar de la tabla
private void calcularTotalPagar() {
    double totalPagar = 0.0;
    for (int i = 0; i < modeloTabla.getRowCount(); i++) {
        totalPagar += (double) modeloTabla.getValueAt(i, 4);
    }
    txtTotalPago.setText(String.format("%.2f", totalPagar));
}
