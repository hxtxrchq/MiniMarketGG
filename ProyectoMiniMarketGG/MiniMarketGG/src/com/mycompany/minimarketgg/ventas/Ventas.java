    private void btnAddTblActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddTblActionPerformed
      try {
            String codigo = txtCodigo.getText();
            String nombre = txtNombre.getText();
            int cantidad = Integer.parseInt(txtCant.getText());
            int stockDisponible = Integer.parseInt(txtStock.getText());
            double precio = Double.parseDouble(txtPrecio.getText());

            if (cantidad <= 0) {
                JOptionPane.showMessageDialog(null, "La cantidad debe ser mayor que cero.");
                return;
            }

            if (cantidad > stockDisponible) {
                JOptionPane.showMessageDialog(null, "La cantidad no puede ser mayor al stock disponible.");
                return;
            }

            // Verificar si el producto ya está en la tabla
            for (int i = 0; i < modeloTabla.getRowCount(); i++) {
                if (modeloTabla.getValueAt(i, 0).toString().equals(codigo)) {
                    JOptionPane.showMessageDialog(null, "El producto ya fue agregado.");
                    return;
                }
            }

            double total = cantidad * precio;

            Object[] fila = {codigo, nombre, cantidad, precio, total};
            modeloTabla.addRow(fila);

            calcularTotalPagar(); // Actualizar el total
            limpiarCampos();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Por favor, coloque un valor correcto para agregar el producto.");
        }
    }//GEN-LAST:event_btnAddTblActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
    int filaSeleccionada = TablaVenta.getSelectedRow();
    if (filaSeleccionada >= 0) {
        try {
            String codigo = modeloTabla.getValueAt(filaSeleccionada, 0).toString();
            int cantidad = Integer.parseInt(JOptionPane.showInputDialog("Ingrese la nueva cantidad:"));

            if (cantidad <= 0) {
                JOptionPane.showMessageDialog(null, "La cantidad debe ser mayor que cero.");
                return;
            }

            double precio = Double.parseDouble(modeloTabla.getValueAt(filaSeleccionada, 3).toString());
            Integer stockDisponibleObj = stockOriginal.get(codigo); 

            if (stockDisponibleObj == null) {
                JOptionPane.showMessageDialog(null, "Stock original no encontrado para el producto con código: " + codigo);
                return;
            }

            int stockDisponible = stockDisponibleObj.intValue();

            if (cantidad > stockDisponible) {
                JOptionPane.showMessageDialog(null, "La cantidad no puede ser mayor al stock disponible.");
                return;
            }

            double total = cantidad * precio;

            modeloTabla.setValueAt(cantidad, filaSeleccionada, 2);
            modeloTabla.setValueAt(total, filaSeleccionada, 4);

            calcularTotalPagar(); // Actualizar el total
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Por favor, coloque un valor correcto para cambiar la cantidad.");
        }
    } else {
        JOptionPane.showMessageDialog(null, "Selecciona un producto para editar.");
    }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
         int filaSeleccionada = TablaVenta.getSelectedRow();
    if (filaSeleccionada >= 0) {
        modeloTabla.removeRow(filaSeleccionada);
        calcularTotalPagar(); // Actualizar el total
    } else {
        JOptionPane.showMessageDialog(null, "Selecciona un producto para eliminar.");
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

    
 
    private void btnPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarActionPerformed
       if (modeloTabla.getRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "No hay productos en la venta. Agrega productos antes de proceder al pago.");
            return;
        }

        double totalPagar = Double.parseDouble(txtTotalPago.getText());
        JFrame ventanaPagos = new JFrame();
        Pagos pagosPanel = new Pagos(TablaVenta, totalPagar, usuario);
        ventanaPagos.setContentPane(pagosPanel);
        ventanaPagos.setSize((int) (PantallaUtil.obtenerAnchoPantalla() * 0.85), (int) (PantallaUtil.obtenerAltoPantalla() * 0.85));
        ventanaPagos.setVisible(true);
        ventanaPagos.setLocationRelativeTo(null);
        ventanaPagos.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);

        // Cerrar la ventana actual
        JFrame currentFrame = (JFrame) SwingUtilities.getWindowAncestor(this);
        if (currentFrame != null) {
            currentFrame.dispose();
        }
    }//GEN-LAST:event_btnPagarActionPerformed
     
